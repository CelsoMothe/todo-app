    public void decorateTableTask() {
        
        jTableTasks.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 18));
        jTableTasks.getTableHeader().setBackground(new Color(0, 153, 102));
        jTableTasks.getTableHeader().setForeground(new Color(255, 255, 255));
        jTableTasks.setAutoCreateRowSorter(true);
        
        jTableTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadlineColumnCellRenderer());
        jTableTasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRenderer("edit"));
        jTableTasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRenderer("delete"));
    }
    
    public void initController() {
        taskController = new TaskController();
        projectController = new ProjectController();
    }
    
    public void initComponentsModel() {
        projectModelList = new DefaultListModel();
        loadProjects();
        
        taskModelList = new TaskTableModel();
        jTableTasks.setModel(taskModelList);
        
        if (!projectModelList.isEmpty()) {
            jListProjects.setSelectedIndex(0);
            Project project = (Project) projectModelList.get(0);
            loadTasks(project.getId());
        }
    }
    
    public void loadTasks(int idProject) {
        List<Task> taskList = taskController.getAll(idProject);
        taskModelList.setTasks(taskList);
        
        showJTableTasks(!taskList.isEmpty());
    }
    
    private void showJTableTasks(boolean hasTasks) {
        
        if (hasTasks) {
            if (jPanelEmptyList.isVisible()) {
                jPanelEmptyList.setVisible(false);
                jPanel5.remove(jPanelEmptyList);
            }
            jPanel5.add(jScrollPaneTasks);
            jScrollPaneTasks.setVisible(true);
            jScrollPaneTasks.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        } else {
            if (jScrollPaneTasks.isVisible()) {
                jScrollPaneTasks.setVisible(false);
                jPanel5.remove(jScrollPaneTasks);
            }
            jPanel5.add(jPanelEmptyList);
            jPanelEmptyList.setVisible(true);
            jPanelEmptyList.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        }
    }
    
    public void loadProjects() {
        
        List<Project> projectsList = projectController.getProject();
        
        projectModelList.clear();
        
        for (int i = 0; i < projectsList.size(); i++) {
            
            Project project = projectsList.get(i);
            projectModelList.addElement(project);
        }

        jListProjects.setModel(projectModelList);
    }
